const contractAddress = '0xd9145CCE52D386f254917e481eB44e9943F39138'; // Kontratınızın adresi

const MintTokenComponent = () => {
    const { address } = useAccount(); // Kullanıcının adresini al

    // 'mintToken' fonksiyonu için hazırlık yap
    const { config, error: prepareError, isIdle } = usePrepareContractWrite({
        addressOrName: contractAddress,
        contractInterface: contractABI,
        functionName: 'mintToken',
        args: [address, 1] // Kullanıcı adresi ve token ID
    });

    const { write, error: writeError } = useContractWrite(config);

    // Tokeni mintleme butonunun tıklanma olayını ele al
    const handleMintButtonClick = () => {
        if (isIdle) {
            write?.()
                .then((transactionResponse) => {
                    // İşlem yanıtını ele al
                    Alert.alert("Success", "Token minting initiated. Check your wallet.");
                })
                .catch((error) => {
                    // Hataları ele al
                    Alert.alert("Error", error.message || "Token minting failed.");
                });
        }
    };

    return (
        <View>
            <Button title="Mint Token" onPress={handleMintButtonClick} />
            {prepareError && <Text>Error: {prepareError.message}</Text>}
            {writeError && <Text>Error: {writeError.message}</Text>}
        </View>
    );
};

export default MintTokenComponent;
